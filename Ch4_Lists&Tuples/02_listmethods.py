# list sort
l1=[1,31,5,27,19]
l1.sort()
print(l1)
l1.reverse()
print(l1)
l1.append(45)# add in list use append() function
print(l1)
l1.insert(2,54)# in this function 2 defines index no to insert 54
print(l1)
l1.pop(2) # in this function value in index 2 will remove
print(l1)
l1.remove(19)# it will remove 19 no from list
print(l1)